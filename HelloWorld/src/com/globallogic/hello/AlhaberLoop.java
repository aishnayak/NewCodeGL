//Java Program to Display Alphabets (A to Z) using loop
package com.globallogic.hello;

public class AlhaberLoop {
  
	public static void main (String args[]) {
		
		char alphabet; //givoing char to alphabet
		
		System.out.println("Display Alphabets from A to Z  \n");
		//For LOOP
		for(alphabet='A';alphabet<='z';alphabet++)
			System.out.println(alphabet + " "); //Printing of alphabets
	}
}
